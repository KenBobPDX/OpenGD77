/**
 * @file    firmware.c
 * @brief   Application entry point.
 */
#include <stdio.h>
#include "board.h"
#include "peripherals.h"
#include "pin_mux.h"
#include "clock_config.h"
#include "MK22F51212.h"

#include "fw_main.h"

/*
 * @brief   Application entry point.
 */
int main(void) {
	SCB->VTOR = 0x4000;

  	/* Init board hardware. */
    BOARD_InitBootPins();
    BOARD_BootClockHSRUN();
    BOARD_InitBootPeripherals();

    fw_init();

    return 0;
}
